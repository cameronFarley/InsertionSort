let STDIN = (readLine(strippingNewline: true))?.split {$0 == " "}.map (String.init)

func insertionSort(_ array: [String]) {
    var a = array                             
    for x in 1..<a.count {                    
        var y = x
        while y > 0 && a[y] < a[y - 1] {
            (a[y], a[y-1]) = (a[y-1], a[y])
            y -= 1           
        }
    }
}

print(insertionSort(STDIN!))
